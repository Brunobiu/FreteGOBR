const STATE_NAME_BY_UF: Record<string, string> = Object.fromEntries(
  BRAZIL_STATES.map((s) => [s.uf, s.name])
);

export function getStateName(uf: string | null | undefined): string | null {
  if (!uf) return null;
  return STATE_NAME_BY_UF[uf.toUpperCase()] ?? null;
}
